
public class GameModifier
	{
		
		private String stacking;
		private String drawTillPlay;
		private String sevenZeroRule;
		
		public GameModifier(String st, String d, String se)
		{
			stacking = st;
			drawTillPlay = d;
			sevenZeroRule = se;
		}

		public String getStacking()
			{
				return stacking;
			}

		public void setStacking(String stacking)
			{
				this.stacking = stacking;
			}

		public String getDrawTillPlay()
			{
				return drawTillPlay;
			}

		public void setDrawTillPlay(String drawTillPlay)
			{
				this.drawTillPlay = drawTillPlay;
			}

		public String getSevenZeroRule()
			{
				return sevenZeroRule;
			}

		public void setSevenZeroRule(String sevenZeroRule)
			{
				this.sevenZeroRule = sevenZeroRule;
			}
		
		
		
		
		
		
		
		
		
	}
